	AREA_STATUS tinyint(1) NOT NULL DEFAULT 1,
	PRIMARY KEY(ID_AREA)
)ENGINE=InnoDB CHARACTER SET utf8 COLLATE utf8_spanish2_ci;

/*DYNAMIC*/
/*
KEY_1 = ID_DEPARTMENT FROM TABLE DEPARTMENT
KEY_2 = ID_AREA FROM TABLE DEPARTENT_AREA_{KEY_1}
 */
